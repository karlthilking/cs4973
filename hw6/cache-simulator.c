/**
 * cache_display_summary(): Display a summary describing the final state of
 *                          the cache by showing the mapping of each line
 *                          to a virtual address
 * @cache: Cache to display the summary of
 * @fd: File descriptor to write to results to because stdout was closed
 *      earlier to silence verbose output
 * @nr_hits: Number of cache hits
 * @nr_misses: Number of cache misses
 */
void cache_display_summary(cache_t *cache, int fd, 
                           int nr_hits, int nr_misses)
{
        char buf[512];
        write(fd, "Cache Summary:\n", sizeof("Cache Summary:\n"));
        for (cache_set_t *set = cache->cache_sets;
             set < cache->cache_sets + cache->nr_sets; set++) {
                snprintf(buf, sizeof(buf), "Cache Set %d:\n", set->index);
                write(fd, buf, strlen(buf));
                u64 index = cache_rebase_index(cache, set->index);
                for (int i = 0; i < cache->nr_set_entries; i++) {
                        cache_line_t *line = set->cache_lines + i;
                        u64 tag = cache_rebase_tag(cache, line->tag);
                        if (line->v) {
                                snprintf(buf, sizeof(buf),
                                         "Cache Line %d, "
                                         "Address: %lx\n", i, tag | index);
                        } else {
                                snprintf(buf, sizeof(buf),
                                         "Cache Line %d is invalid\n", i);
                        }
                        write(fd, buf, strlen(buf));
                }
        }
        snprintf(buf, sizeof(buf), 
                 "\nCache Performance:\n"
                 "Number of total memory access:\t%d\n"
                 "Number of total hits:\t\t%d\n"
                 "Number of total misses:\t\t%d\n"
                 "Percent cache hits:\t\t%f\n\n",
                 nr_hits + nr_misses, nr_hits, nr_misses,
                 (float)nr_hits / (float)(nr_hits + nr_misses));
        write(fd, buf, strlen(buf));
}

/*
 * cache_simulate(): Simulates cache with reads and writes reported from
 *                   binary instrumented with llvm module pass
 * @cache: Cache to use for simulation
 * @return: 1 if simulation is successful, 0 if error occurs
 */
int cache_simulate(cache_t *cache, char mode)
{       
        /* Disable stdout during simulation if in summary mode */
        int fd;
        if (mode == 's') {
                fd = open("/dev/stdout", O_WRONLY);
                if (fd < 0) {
                        perror("open");
                        return -1;
                }
                close(STDOUT_FILENO);
        }

        int nr_hits = 0, nr_misses = 0; // number of cache hits and misses
        u64  addr;                      // address of read or write
        char rw;                        // if access was a read or write
        
        cache_set_t *set;
        while (!feof(stdin)) {
                if (scanf("%c%lx\n", &rw, &addr) < 2) {
                        fprintf(stderr, "Failed to retrieve load/store"
                                        "info\n");
                        return -1;
                }
                /* Index into set corresponding to the address index bits */
                set = cache->cache_sets + cache_extract_index(cache, addr);
                if (cache_line_search(cache, set, addr, rw))
                        nr_hits++;
                else {
                        nr_misses++;
                        int rc = cache_replace_line(cache, set, addr, rw);
                        if (!rc) {
                                fprintf(stderr, "Cache eviction failed\n");
                                return -1;
                        }
                }
        }
        
        /* Display of summary */
        if (mode == 's')
                cache_display_summary(cache, fd, nr_hits, nr_misses);
        return 0;
}

int main(int argc, char *argv[])
{
        if (argc < 7) {
                fprintf(stderr, "Usage: ./cache-simulator "
                                "--data-block-size [N] "
                                "--cache-size [N] "
                                "--mode [verbose | summary] "
                                "--associativity [N] (optional)\n");
                exit(1);
        }
        
        u8 data_block_size = 0;
        u16 cache_size = 0;
        u8 associativity = 0;
        char mode = 0;
        
        for (int i = 1; i < argc; i += 2) {
                if (!strcmp("--data-block-size", argv[i]))
                        data_block_size = atoi(argv[i + 1]);
                else if (!strcmp("--cache-size", argv[i]))
                        cache_size = atoi(argv[i + 1]);
                else if (!strcmp("--mode", argv[i]))
                        mode = (strcmp(argv[i + 1], "verbose")) ? 's' : 'v';
                else if (!strcmp("--associativity", argv[i]))
                        associativity = atoi(argv[i + 1]);
        }
        
        if (!(data_block_size && cache_size && mode)) {
                fprintf(stderr, "A data block size, cache size or mode "
                                "were not selected\n");
                exit(1);
        } else if (associativity == 0) {
                /* Default to fully associative cache */
                associativity = cache_size / data_block_size;
        }

        cache_t *cache = cache_create(cache_size, data_block_size, 
                                      associativity);
        if (cache_simulate(cache, mode) < 0) {
                fprintf(stderr, "cache-simulation failed. Exiting...\n");
                cache_destroy(cache);
                exit(1);
        }

        cache_destroy(cache);
        exit(0);
}
